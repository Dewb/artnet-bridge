impl Header {
    fn with_command(command_code: u16) -> Self {
        Header {
            magic: 0x0401dc4a,
            version: 0x0100,
            command: command_code.swap_bytes(),
            sequence: 0x00000000,
            port: 0,
            padding: 0,
            flags: 0x0000,
        }
    }
}

impl Default for DmxOut {
    fn default() -> Self {
        DmxOut {
            header: Header::with_command(0x101),
            timer: -1,
            universe: 0,
            data: [0; 512],
        }
    }
}

impl Default for PortOut {
    fn default() -> Self {
        PortOut {
            header: Header::with_command(0x108),
            port: 1,
            padding: 0,
            port_out_flags: 0x0001,
            length: (0xFF as u16).swap_bytes(),
            start_code: (0x0FFF as u16).swap_bytes(),
            data: [0; 512],
        }
    }
}

impl DmxOut {
    pub fn serialize(self) -> Result<Vec<u8>, Error> {
        return Ok(bincode::config().big_endian().serialize(&self)?);
    }
}

impl PortOut {
    pub fn serialize(self) -> Result<Vec<u8>, Error> {
        return Ok(bincode::config().big_endian().serialize(&self)?);
    }
}


