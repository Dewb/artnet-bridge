use serde::{Serialize, Serializer};
use serde::ser::SerializeTuple;
use anyhow::Error;

extern crate bincode;


#[derive(Serialize)]
pub struct Header {
    pub magic: i32,
    pub version: u16,
    pub command: u16,
    pub sequence: i32,
    pub port: u8,
    pub padding: u8,
    pub flags: u16,
}

#[derive(Serialize)]
pub struct DmxOut {
    pub header: Header,
    pub timer: i32,
    pub universe: u8,
    #[serde(serialize_with = "serialize_array512")]
    pub data: [u8; 512],
}

#[derive(Serialize)]
pub struct PortOut {
    pub header: Header,
    pub port: u8,
    pub padding: u8,
    pub port_out_flags: u16,
    pub length: u16,
    pub start_code: u16,
    #[serde(serialize_with = "serialize_array512")]
    pub data: [u8; 512],
}

fn serialize_array512<S, T>(array: &[T; 512], serializer: S) -> Result<S::Ok, S::Error>
where
    S: Serializer,
    T: Serialize,
{
    let mut seq = serializer.serialize_tuple(512)?;
    for element in array.iter() {
        seq.serialize_element(element)?;
    }
    return seq.end();
}

